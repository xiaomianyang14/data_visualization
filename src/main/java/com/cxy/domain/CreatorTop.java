package com.cxy.domain;

public class CreatorTop {
    private int id;
    private String creator_name;
    private int creator_count;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCreator_name() {
        return creator_name;
    }

    public void setCreator_name(String creator_name) {
        this.creator_name = creator_name;
    }

    public int getCreator_count() {
        return creator_count;
    }

    public void setCreator_count(int creator_count) {
        this.creator_count = creator_count;
    }
}
