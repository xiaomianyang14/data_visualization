package com.cxy.domain;

public class YearSum {
    private int id;
    private int year_name;
    private int year_count;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getYear_name() {
        return year_name;
    }

    public void setYear_name(int year_name) {
        this.year_name = year_name;
    }

    public int getYear_count() {
        return year_count;
    }

    public void setYear_count(int year_count) {
        this.year_count = year_count;
    }
}
