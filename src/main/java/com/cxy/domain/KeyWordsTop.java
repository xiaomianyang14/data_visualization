package com.cxy.domain;

public class KeyWordsTop {
    private int id;
    private String keywords_name;
    private int keywords_count;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKeywords_name() {
        return keywords_name;
    }

    public void setKeywords_name(String keywords_name) {
        this.keywords_name = keywords_name;
    }

    public int getKeywords_count() {
        return keywords_count;
    }

    public void setKeywords_count(int keywords_count) {
        this.keywords_count = keywords_count;
    }
}
